// Version of the agent - update this with each release
const Version = "v1.1.6"

type TrayApp struct {
	device *device.Device
	onExit func()
}

func New(dev *device.Device, onExit func()) *TrayApp {
	return &TrayApp{
		device: dev,
		onExit: onExit,
	}
}

func (t *TrayApp) Run() {
	systray.Run(t.onReady, t.onExit)
}

func (t *TrayApp) onReady() {
	// Set up the system tray icon and menu
	systray.SetIcon(getIcon())
	systray.SetTitle("Remote")
	systray.SetTooltip(fmt.Sprintf("Remote Desktop Agent\nDevice: %s", t.device.Name))

	// Add menu items
	mDeviceName := systray.AddMenuItem(fmt.Sprintf("Device: %s", t.device.Name), "")
	mDeviceName.Disable()
	
	mStatus := systray.AddMenuItem("Status: Online", "Current connection status")
	mStatus.Disable()
	
	systray.AddSeparator()
	
	mConsole := systray.AddMenuItem("Show Console Window", "Open live console output")
	mLogs := systray.AddMenuItem("View Log File", "Open log file in editor")
	mVersion := systray.AddMenuItem(fmt.Sprintf("Version %s", Version), "Agent version")
	mVersion.Disable()
	
	systray.AddSeparator()
	
	mQuit := systray.AddMenuItem("Exit", "Quit the agent")

	// Handle menu clicks
	go func() {
		for {
			select {
			case <-mConsole.ClickedCh:
				openConsole()
			case <-mLogs.ClickedCh:
				openLogFile()
			case <-mQuit.ClickedCh:
				log.Println("ðŸ›‘ Exit requested from system tray")
				systray.Quit()
				return
			}
		}
	}()
}

func openConsole() {
	// Get the executable directory
	exePath, err := os.Executable()
	if err != nil {
		log.Printf("Failed to get executable path: %v", err)
		return
	}
	exeDir := filepath.Dir(exePath)
	logPath := filepath.Join(exeDir, "agent.log")

	// Open a PowerShell window that tails the log file
	log.Println("ðŸªŸ Opening console window with live logs...")
	
	// Use PowerShell to tail the log file in a new window
	psCmd := fmt.Sprintf(`Get-Content '%s' -Wait -Tail 50`, logPath)
	cmd := exec.Command("powershell", "-NoExit", "-Command", psCmd)
	
	if err := cmd.Start(); err != nil {
		log.Printf("Failed to open console: %v", err)
	}
}

func openLogFile() {
	// Get the executable directory
	exePath, err := os.Executable()
	if err != nil {
		log.Printf("Failed to get executable path: %v", err)
		return
	}
	exeDir := filepath.Dir(exePath)
	logPath := filepath.Join(exeDir, "agent.log")

	// Check if log file exists
	if _, err := os.Stat(logPath); os.IsNotExist(err) {
		log.Printf("Log file does not exist: %s", logPath)
		return
	}

	// Open log file with default text editor
	log.Printf("ðŸ“„ Opening log file: %s", logPath)
	
	// Use notepad as it's always available on Windows
	cmd := exec.Command("notepad", logPath)
	if err := cmd.Start(); err != nil {
		log.Printf("Failed to open log file: %v", err)
	}
}

